def match_words(words):
    ctr=0
    list1=[]
    for word in words:
        if len(word)>1 and word[0] == word[-1]:
            ctr+=1
            list1.append(word)
    print("List of words with the same first and last letter: ", list1)
    return ctr
count=match_words(["abc", "def", "ghi", "jkl", "mno", "pqr", "stu", "wxyz", "heh", "apple", "cdc", "dod"])
print("Number of words having the same first and last character: ", count)
    